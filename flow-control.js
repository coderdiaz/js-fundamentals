//  Flow-control
// If-sentences
var valid = true;
if (valid) {
  console.log('Is valid');
}

// If-else sentences
valid = false;
if (valid) {
  console.log('Is valid');
} else {
  console.log('Is not valid');
}

// if-else-if
var number = 1;
if (number === 0) {
  console.log('The number is 0');
} else if (number > 0 && number <= 100) {
  console.log('The number is greater than 0');
} else {
  console.log('The number is greater than 100');
}
